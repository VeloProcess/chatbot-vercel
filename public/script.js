        // Processar √°udio para texto
        async function processAudioToText(audioBlob) {
            try {
                addMessage('üé§ Processando √°udio...', 'bot');
                
                console.log('üé§ Tipo do audioBlob:', typeof audioBlob);
                console.log('üé§ audioBlob:', audioBlob);
                
                // Verificar se √© um Blob v√°lido
                if (!audioBlob || typeof audioBlob.arrayBuffer !== 'function') {
                    throw new Error('AudioBlob inv√°lido ou n√£o √© um Blob');
                }
                
                // Converter blob para base64
                const arrayBuffer = await audioBlob.arrayBuffer();
                console.log('üé§ ArrayBuffer criado, tamanho:', arrayBuffer.byteLength);
                
                // Converter para base64 de forma mais segura
                const uint8Array = new Uint8Array(arrayBuffer);
                let binaryString = '';
                const chunkSize = 8192; // Processar em peda√ßos para evitar stack overflow
                
                for (let i = 0; i < uint8Array.length; i += chunkSize) {
                    const chunk = uint8Array.slice(i, i + chunkSize);
                    binaryString += String.fromCharCode.apply(null, chunk);
                }
                
                const base64Audio = btoa(binaryString);
                console.log('üé§ Base64 criado, tamanho:', base64Audio.length);
                
                const response = await fetch('/api/voice?action=speech-to-text', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify({
                        audio: base64Audio
                    })
                });

                if (!response.ok) {
                    const errorText = await response.text();
                    throw new Error(`Erro na API: ${response.status} - ${errorText}`);
                }

                const result = await response.json();

                if (result.success) {
                    addMessage(`üé§ Voc√™ disse: "${result.text}"`, 'user');
                    buscarResposta(result.text);
                } else {
                    console.error('‚ùå Erro na transcri√ß√£o:', result);
                    if (result.error && result.error.includes('legenda')) {
                        addMessage(`‚ùå √Åudio n√£o reconhecido. Tente falar mais claro ou verifique se n√£o h√° ru√≠do de fundo.`, 'bot');
                    } else if (result.error && result.error.includes('vazio')) {
                        addMessage(`‚ùå Nenhum √°udio detectado. Verifique se o microfone est√° funcionando.`, 'bot');
                    } else if (result.error && result.error.includes('Buffer')) {
                        addMessage(`‚ùå Problema com o √°udio. Tente gravar novamente.`, 'bot');
                    } else {
                        addMessage(`‚ùå Erro na transcri√ß√£o: ${result.error}`, 'bot');
                    }
                }

            } catch (error) {
                console.error('‚ùå Erro ao processar √°udio:', error);
                addMessage(`‚ùå Erro ao processar √°udio: ${error.message}`, 'bot');
            }
        }

